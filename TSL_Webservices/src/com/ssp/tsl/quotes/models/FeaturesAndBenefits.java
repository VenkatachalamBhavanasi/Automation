package com.ssp.tsl.quotes.models;

public class FeaturesAndBenefits {
	private String code;
	private String description;
    private Integer featuresAndBenefitsId;
    private String limitCover;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Integer getFeaturesAndBenefitsId() {
		return featuresAndBenefitsId;
	}
	public void setFeaturesAndBenefitsId(Integer featuresAndBenefitsId) {
		this.featuresAndBenefitsId = featuresAndBenefitsId;
	}
	public String getLimitCover() {
		return limitCover;
	}
	public void setLimitCover(String limitCover) {
		this.limitCover = limitCover;
	}
    

}
